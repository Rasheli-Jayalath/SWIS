					<h3>

						INTRODUCTION<span>Detailed Project Report for In-Village System Infrastructure rural water supply works in Karnataka</span></h3>
					<p>The project is an outcome of Central Government's Jal Jeevan Mission and aims to cover several districts of Karnataka under Piped Drinking Water Supply scheme.</p>
					<p>The project aims to achieve following outcomes :</p>
					<ul>
						<li>
							Outlet from the OHT/GLSR: The in-village infrastructure with respect to drinking water distribution system is proposed to start from the outlet of overhead tank or the ground level service reservoir which is in use, and has a flow meter installed at the inlet.</li>
						<li>Drinking water distribution system pipelines in the existing road network: The water distribution system pipelines of single village/ Multi village schemes with the OHT/ GLSR as the service point and embedded in the road network</li>
						<li>Existing household tap connections/ Cisterns: This refers to the household tap connections from the laterals/ pipelines running along the roads and connected to households by means of a ferrule and a saddle/ electrofusion fittings/ PVC fittings/ GI fittings. </li>
						<li>Identification and analysis of an existing source for the scheme.</li>
						<li>Water storage in a village: Water storage is proposed in the form of overhead tank or a ground level sump/ tank or intermediate pumping station with a ground level sump.</li>
					</ul>
